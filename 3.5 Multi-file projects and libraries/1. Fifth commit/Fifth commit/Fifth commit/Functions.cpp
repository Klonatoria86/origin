#include<cmath>
#include"functions.h"
double Addition(double a, double b) {
	return a + b;
}
double Subtraction(double a, double b) {
	return a - b;
}
double Multiplication(double a, double b) {
	return a * b;
}
double Separation(double a, double b) {
	return a / b;
}
double Exponentiation(double a, double b) {
	return pow(a, b);
}